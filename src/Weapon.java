public class Weapon {
    private String typeWeapon;
    private String name;

    public void setTypeWeapon(String typeWeapon) {
        this.typeWeapon = typeWeapon;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTypeWeapon() {
        return typeWeapon;
    }

    public String getName() {
        return name;
    }
}
